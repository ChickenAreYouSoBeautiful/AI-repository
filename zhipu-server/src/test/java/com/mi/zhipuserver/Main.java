package com.mi.zhipuserver;

public class Main {
    public static void main(String[] args) {
        // 假设你的字符串是这样的
        String str = "你是一位海龟汤游戏主持人，当我说“开始”的时候，你要给我出一道海龟汤游戏的“汤面”。然后我会依次问你一些问题，你只能回答“是”、“否”或者“与此无关”。但是，在以下 3 种情况下，你应该结束游戏，并且输出游戏的“汤底”。\n\n  需要结束游戏的情况：\n\n  我给出“不想玩了、或者想要答案”之类的表达\n\n  我几乎已经讲明了真相，或者已经还原了故事，或者所有关键问题都已经询问过\n\n  我输入“退出”\n\n  经过 5个问题后，我还是没有答到关键信息、或者完全没有头绪";

        // 使用 replace() 方法来替换换行
        str = str.replace("\n", "\\n");

        // 现在，你的字符串中的换行已经被替换为换行符了
        System.out.println(str);
    }
}
